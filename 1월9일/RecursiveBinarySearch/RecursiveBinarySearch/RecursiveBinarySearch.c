#include<stdio.h>

int BSearchBecur(int ar[], int first, int last, int target)
{
	int mid;
	if (first > last)
		return -1;		//-1은 탐색 실패를 의미한다.
	else		//생략가능
		mid = (first + last) / 2;		//탐색대상의 중앙을 찾는다.

	if (ar[mid] == target)	//탐색된 타겟의 인덱스 값 반환
		return mid;
	else if (target < ar[mid])
		return BSearchBecur(ar, first, mid - 1, target);
	else
		return BSearchBecur(ar, mid + 1, last, target);
}

int main(void)
{
	int arr[] = { 1,3,5,7,9 };
	int idx;

	idx = BSearchBecur(arr, 0, sizeof(arr) / sizeof(int) - 1, 7);
	if (idx == -1)		//-1은 탐색 실패를 의미한다. :: 7줄
		printf("탐색실패\n");
	else
		printf("타겟 저장 인덱스 : %d \n", idx);

	idx = BSearchBecur(arr, 0, sizeof(arr) / sizeof(int) - 1, 4);
	if (idx == -1)
		printf("탐색실패\n");
	else
		printf("타겟 저장 인덱스 : %d \n", idx);

	return 0;

}